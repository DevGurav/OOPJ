public class ForLoop 
{
    public static void main(String[] args) 
    {
        for (int i = 1; i <= 5; i++) 
        {
            System.out.println("Value of i: " + i);
        }
    }
}
/*
OUTPUT-
Value of i: 1
Value of i: 2
Value of i: 3
Value of i: 4
Value of i: 5    
*/    
