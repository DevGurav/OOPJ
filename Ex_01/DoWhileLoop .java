public class DoWhileLoop 
{
    public static void main(String[] args) 
    {
        int i = 1;
        
        do 
        {
            System.out.println("Value of i: " + i);
            i++;
        } while (i <= 5);
    }
}

/*
OUTPUT -
        Value of i: 1
        Value of i: 2 
        Value of i: 3
        Value of i: 4
        Value of i: 5
*/
