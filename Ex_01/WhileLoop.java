public class WhileLoop 
{
    public static void main(String[] args) 
    {
        int i = 1;
        
        while (i <= 5) 
        {
            System.out.println("Value of i: " + i);
            i++;
        }
    }
}
/*
output
Value of i: 1
Value of i: 2
Value of i: 3
Value of i: 4
Value of i: 5
*/    
